package com.hyperion.model

data class Channel(
    val allowedRegions: List<String>,
    val author: String,
    val authorBanners: List<AuthorBanner>,
    val authorId: String,
    val authorThumbnails: List<AuthorThumbnail>,
    val authorUrl: String,
    val autoGenerated: Boolean,
    val description: String,
    val descriptionHtml: String,
    val isFamilyFriendly: Boolean,
    val joined: Long,
    val latestVideos: List<LatestVideo>,
    val paid: Boolean,
    val relatedChannels: List<RelatedChannel>,
    val subCount: Int,
    val totalViews: Long
) {
    data class AuthorBanner(
        val height: Int,
        val url: String,
        val width: Int
    )

    data class LatestVideo(
        val author: String,
        val authorId: String,
        val authorUrl: String,
        val description: String,
        val descriptionHtml: String,
        val lengthSeconds: String,
        val paid: String,
        val premium: String,
        val published: String,
        val publishedText: String,
        val title: String,
        val videoId: String,
        val videoThumbnails: List<VideoThumbnail>,
        val viewCount: Int
    )

    data class RelatedChannel(
        val author: String,
        val authorId: String,
        val authorThumbnails: List<AuthorThumbnail>,
        val authorUrl: String
    )
}